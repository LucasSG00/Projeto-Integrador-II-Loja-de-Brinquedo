    private void btnPrCadastrarSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrCadastrarSalvarActionPerformed
        try{
            /* Irar no banco de dados para salvar os dados */
            if("".equals(txtPrCadastrarNome.getText())) {
                JOptionPane.showMessageDialog(this, "Campo 'Nome' em Branco");            
            }
            if("".equals(txtPrCadastrarCor.getText())) {
                JOptionPane.showMessageDialog(this, "Campo 'Cor' em Branco");            
            }
            if("".equals(txtPrCadastrarCategoria.getText())) {
                JOptionPane.showMessageDialog(this, "Campo 'Categoria' em Branco");            
            }
            if("".equals(txtPrCadastrarDimensao.getText())) {
                JOptionPane.showMessageDialog(this, "Campo 'Dimens√£o' em Branco");            
            }
            
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso");
            ViewProdutoCadastra.this.dispose();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro no Cadastro, por favor verificar todos os campos");
        }

        /* Fazer implementa√ß√£o com o Banco de Dados */
    }//GEN-LAST:event_btnPrCadastrarSalvarActionPerformed


